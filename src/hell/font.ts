/**
 * Interface for Feld-Hell bitmap fonts.
 * Designed to be implemented by both hardcoded fonts and future BDF parser.
 */
export interface HellFont {
  /** Glyph height in pixels (typically 7 for Feld-Hell) */
  readonly height: number;

  /**
   * Get glyph pixel grid for a character.
   * Returns number[][] where each inner array is a row (top to bottom),
   * and each element is 1 (tone on) or 0 (silence).
   * Columns go left to right within each row.
   * Returns undefined for unknown characters.
   */
  getGlyph(char: string): number[][] | undefined;
}

